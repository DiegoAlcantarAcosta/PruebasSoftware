package casoDeUsoEliminar;

public class CasoDeUsoEliminarException extends Exception{

    public CasoDeUsoEliminarException() {
    }

    public CasoDeUsoEliminarException(String message) {
        super(message);
    }

    public CasoDeUsoEliminarException(String message, Throwable cause) {
        super(message, cause);
    }

    public CasoDeUsoEliminarException(Throwable cause) {
        super(cause);
    }

    public CasoDeUsoEliminarException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
    
}
