package casoDeUsoIniciarSesion;

public class CasoDeUsoIniciarSesionException extends Exception{

    public CasoDeUsoIniciarSesionException() {
    }

    public CasoDeUsoIniciarSesionException(String message) {
        super(message);
    }

    public CasoDeUsoIniciarSesionException(String message, Throwable cause) {
        super(message, cause);
    }

    public CasoDeUsoIniciarSesionException(Throwable cause) {
        super(cause);
    }

    public CasoDeUsoIniciarSesionException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

    
}
