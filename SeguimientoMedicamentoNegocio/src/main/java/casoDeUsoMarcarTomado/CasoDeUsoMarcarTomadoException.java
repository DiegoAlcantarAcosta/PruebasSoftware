package casoDeUsoMarcarTomado;

public class CasoDeUsoMarcarTomadoException extends Exception{

    public CasoDeUsoMarcarTomadoException() {
    }

    public CasoDeUsoMarcarTomadoException(String message) {
        super(message);
    }

    public CasoDeUsoMarcarTomadoException(String message, Throwable cause) {
        super(message, cause);
    }

    public CasoDeUsoMarcarTomadoException(Throwable cause) {
        super(cause);
    }

    public CasoDeUsoMarcarTomadoException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
    
}
