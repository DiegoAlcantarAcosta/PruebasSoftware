package casoDeUsoObtener;

public class CasoDeUsoObtenerException extends Exception{

    public CasoDeUsoObtenerException() {
    }

    public CasoDeUsoObtenerException(String message) {
        super(message);
    }

    public CasoDeUsoObtenerException(String message, Throwable cause) {
        super(message, cause);
    }

    public CasoDeUsoObtenerException(Throwable cause) {
        super(cause);
    }

    public CasoDeUsoObtenerException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
    
}
