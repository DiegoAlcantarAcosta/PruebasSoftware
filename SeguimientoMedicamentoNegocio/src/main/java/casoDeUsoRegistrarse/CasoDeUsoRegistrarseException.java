package casoDeUsoRegistrarse;

public class CasoDeUsoRegistrarseException extends Exception{

    public CasoDeUsoRegistrarseException() {
    }

    public CasoDeUsoRegistrarseException(String message) {
        super(message);
    }

    public CasoDeUsoRegistrarseException(String message, Throwable cause) {
        super(message, cause);
    }

    public CasoDeUsoRegistrarseException(Throwable cause) {
        super(cause);
    }

    public CasoDeUsoRegistrarseException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
    
}
